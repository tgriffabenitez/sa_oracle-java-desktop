    private void btnConectarDBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectarDBActionPerformed
        try {
          this.usuario = comboListaUsuario.getSelectedItem().toString();
          this.puerto = inputPuerto.getText();
          this.ip = inputDBIp.getText();

          ConexionDB conexionDB = new ConexionDB(usuario, puerto, ip);
          this.conexion = conexionDB.getConnection();
          MenuTablas menuTablas = new MenuTablas(this);
          menuTablas.setVisible(true);

        } catch (Exception e) {
          JOptionPane.showMessageDialog(this,
              "Error al conectar a la base de datos:\n" + e.getMessage(),
               "Error de conexi√≥n",
               JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnConectarDBActionPerformed

